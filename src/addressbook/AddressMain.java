    private void viewButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewButtonActionPerformed
        String info[] = new String[5];
        
        if (addressList.getSelectedIndex() != -1) 
        {
            info = person.grabEntry(data.get(addressList.getSelectedIndex())[0], data.get(addressList.getSelectedIndex())[1]);
            ViewInformation v1 = new ViewInformation (person, info, addressList.getSelectedIndex());
            v1.setVisible(true);
            this.dispose();
        }
        else
        {
            errorLabel.setText("Select a user to view");
            errorLabel.setVisible(true);
        }
    }//GEN-LAST:event_viewButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        errorLabel.setVisible(false);
        errorLabel2.setVisible(false);
        
        person.removeEntry(addressList.getSelectedIndex());
        listModel.remove(addressList.getSelectedIndex());
    }//GEN-LAST:event_deleteButtonActionPerformed

