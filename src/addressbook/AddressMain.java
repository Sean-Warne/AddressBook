    private void encryptExitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_encryptExitButtonActionPerformed
        if (true || listModel.size() == 0)
        {
            //PasswordEntry p1 = new PasswordEntry();
            //p1.setVisible(true);
            
            person.writeData(passwordField.getText());
            this.dispose();
        }
        else
        {
            PasswordEntry p1 = new PasswordEntry();
            String key = passwordField.getText();
            
            if (!key.equals(""))
            {
                person.writeData(key);
                p1.setVisible(true);
                this.dispose();
            }
            else
            {
                errorLabel2.setText ("Enter passkey to encrypt and exit");
                errorLabel2.setVisible(true);
            }
        }
    }//GEN-LAST:event_encryptExitButtonActionPerformed

